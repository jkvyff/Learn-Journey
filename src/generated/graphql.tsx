import gql from 'graphql-tag';
import * as ApolloReactCommon from '@apollo/react-common';
import * as ApolloReactHooks from '@apollo/react-hooks';
export type Maybe<T> = T | null;
/** All built-in and custom scalars, mapped to their actual values */
export type Scalars = {
  ID: string,
  String: string,
  Boolean: boolean,
  Int: number,
  Float: number,
};

export type LoginResponse = {
   __typename?: 'LoginResponse',
  accessToken: Scalars['String'],
  user: User,
};

export type Mutation = {
   __typename?: 'Mutation',
  logout: Scalars['Boolean'],
  revokeRefreshTokensForUser: Scalars['Boolean'],
  login: LoginResponse,
  register: Scalars['Boolean'],
  createResource: Resource,
  updateResource: Scalars['Boolean'],
  deleteResource: Scalars['Boolean'],
};


export type MutationRevokeRefreshTokensForUserArgs = {
  userId: Scalars['Int']
};


export type MutationLoginArgs = {
  password: Scalars['String'],
  email: Scalars['String']
};


export type MutationRegisterArgs = {
  password: Scalars['String'],
  username: Scalars['String'],
  email: Scalars['String']
};


export type MutationCreateResourceArgs = {
  options: ResourceInput
};


export type MutationUpdateResourceArgs = {
  input: ResourceUpdateInput,
  id: Scalars['Int']
};


export type MutationDeleteResourceArgs = {
  id: Scalars['Int']
};

export type Query = {
   __typename?: 'Query',
  hello: Scalars['String'],
  helloLock: Scalars['String'],
  users: Array<User>,
  me?: Maybe<User>,
  resources: Array<Resource>,
};


export type QueryResourcesArgs = {
  skip?: Maybe<Scalars['Int']>,
  take?: Maybe<Scalars['Int']>
};

export type Resource = {
   __typename?: 'Resource',
  id: Scalars['Int'],
  title: Scalars['String'],
  author: Scalars['String'],
  exerpt: Scalars['String'],
  resolved_url: Scalars['String'],
  time_length: Scalars['Int'],
};

export type ResourceInput = {
  title: Scalars['String'],
  author: Scalars['String'],
  exerpt: Scalars['String'],
  date_updated: Scalars['String'],
  resolved_url: Scalars['String'],
  time_length: Scalars['Int'],
};

export type ResourceUpdateInput = {
  title: Scalars['String'],
  author?: Maybe<Scalars['String']>,
  exerpt?: Maybe<Scalars['String']>,
  resolved_url: Scalars['String'],
  time_length?: Maybe<Scalars['Int']>,
};

export type User = {
   __typename?: 'User',
  id: Scalars['Int'],
  email: Scalars['String'],
  username: Scalars['String'],
};

